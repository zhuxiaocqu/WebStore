package com.webstore.bean;

/**
 * 作为ProductBigType UserType ProductSmallType ProductDetailType等类型 类   的父类
 * @author zhuxiao
 *
 */
public class Type {
	private int TypeID;
	private String TypeName;
	
	/**
	 * 获得类型的ID
	 * @return int
	 */
	public int getTypeID() {
		return TypeID;
	}
	public void setTypeID(int typeID) {
		TypeID = typeID;
	}
	/**
	 * 获得类型的Name
	 * @return String
	 */
	public String getTypeName() {
		return TypeName;
	}
	public void setTypeName(String typeName) {
		TypeName = typeName;
	}
	
}
